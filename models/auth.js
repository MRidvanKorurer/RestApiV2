const mongoose = require("mongoose");


const authSchema = mongoose.Schema({
    username: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        trim: true
    },
    password: {
        type: String,
        required: true,
        trim: true
    }
})

const Auth = mongoose.model("auth", authSchema);

module.exports = {Auth};